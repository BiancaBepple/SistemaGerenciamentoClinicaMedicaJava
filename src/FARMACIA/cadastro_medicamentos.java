    public void preencherfornecedores() {
        try {
            //CONSULTAR PACIENTES CADASTRADOS
            Connection conn = null;
            conexao bd = new conexao();
            conn = bd.getConnection();
            Statement stmt = null;
            ResultSet rs = null;

            String sqlConsulta = "Select nome from fornecedores order by nome";
            stmt = conn.createStatement();
            rs = stmt.executeQuery(sqlConsulta);
            fornecedores.removeAllItems();
            while (rs.next()) {
                fornecedores.addItem(rs.getString("nome"));

            }
            rs.close();
            stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(cadastro_medicamentos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void consulta() {
        while (tabela.getModel().getRowCount() > 0) {
            ((DefaultTableModel) tabela.getModel()).removeRow(0);
        }
        try {
            Connection conn = null;
            conexao bd = new conexao();
            conn = bd.getConnection();

            Statement stmt = null;
            ResultSet rs = null;
            String sqlConsulta = "Select * from medicamentos order by idmedicamentos";
            stmt = conn.createStatement();
            rs = stmt.executeQuery(sqlConsulta);
            while (rs.next()) {
                String recebe_id = rs.getString("idmedicamentos");
                String recebe_nome = rs.getString("nome");
                String recebe_laboratorio = rs.getString("laboratorio");
                String recebe_quantidade = rs.getString("quantidade");
                String recebe_validade = rs.getString("validade");

                // Mostra os valores na JTable
                
                ConverteData conv = new ConverteData();
                String data_convertida = conv.DataMostra(recebe_validade);
                
                DefaultTableModel grid = (DefaultTableModel) tabela.getModel();
                grid.addRow(new String[]{recebe_id, recebe_nome, recebe_laboratorio, recebe_quantidade, data_convertida});

            }
        } catch (SQLException ex) {
            Logger.getLogger(cadastro_medicamentos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

