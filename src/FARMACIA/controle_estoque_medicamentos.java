    private void altquantidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_altquantidadeActionPerformed
      String recebe_quantidade = qtmedicamentos.getText();
      int quanti = Integer.parseInt(recebe_quantidade);

      String var_cod;
        int var_Lin = 0;
        var_Lin = tabela.getSelectedRow(); //retorna um inteiro
        var_cod = tabela.getValueAt(var_Lin, 0).toString(); // Valor Chave da Tabela
        int codid = Integer.parseInt(var_cod);
        Connection conn = null;
   
            conexao bd = new conexao();
            conn = bd.getConnection();
        principal_farmacia farm = new principal_farmacia();
        int quantdisponivel =Integer.parseInt(( tabela.getValueAt(var_Lin, 2).toString()));
        
        if(quanti < quantdisponivel){
        
        try {
            farm.diminuirestoque(conn, codid, quanti);
             JOptionPane.showMessageDialog(null,"Quantidade retirada do estoque com sucesso!");
            consulta();
        } catch (SQLException ex) {
            System.out.println("não rolou bi");
            Logger.getLogger(controle_estoque_medicamentos.class.getName()).log(Level.SEVERE, null, ex);
        }}
        else if(quanti == quantdisponivel){
          try {
              farm.excluir(conn, codid);
              JOptionPane.showMessageDialog(null,"Quantidade retirada do estoque com sucesso!");
              consulta();
          } catch (SQLException ex) {
              Logger.getLogger(controle_estoque_medicamentos.class.getName()).log(Level.SEVERE, null, ex);
          }
                }
        else if(quanti > quantdisponivel){
        JOptionPane.showMessageDialog(null, "Quantidade maior do que a disponível em estoque, tente novamente com um valor igual ou menor que a quantidade disponível!");
        }
        
        consulta();
        
    }//GEN-LAST:event_altquantidadeActionPerformed

