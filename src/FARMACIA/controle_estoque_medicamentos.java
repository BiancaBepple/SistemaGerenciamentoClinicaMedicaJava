    public void consulta() {
            while (tabela.getModel().getRowCount() > 0) {
            ((DefaultTableModel) tabela.getModel()).removeRow(0);
        }
        try {
            
            Connection conn = null;
            conexao bd = new conexao();
            conn = bd.getConnection();
            Statement stmt = null;
            ResultSet rs = null;
            String sqlConsulta = "Select idmedicamentos, nome, quantidade from medicamentos order by idmedicamentos";
            
            stmt = conn.createStatement();
            rs = stmt.executeQuery(sqlConsulta);
            while (rs.next()) {
                String recebecodigo = rs.getString("idmedicamentos");
                String recebenome = rs.getString("nome");
                String recebequanti = rs.getString("quantidade");
                
                DefaultTableModel grid = (DefaultTableModel) tabela.getModel();
                grid.addRow(new String[]{recebecodigo, recebenome, recebequanti});
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(cadastro_fornecedores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
