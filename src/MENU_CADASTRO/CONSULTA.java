        try {
            cadastroconsulta cad = new cadastroconsulta();
            String recebepaciente = nome.getText();
            String recebemedico = nomemedico.getText();
            String recebedata =  data.getText();
            String recebehorario = horario.getText();
            String recebesintomas = sintomas.getText();

            ConverteData conv = new ConverteData();
            String data_convertida = conv.DataGrava(recebedata);
            cad.setdata(data_convertida);

            Connection conn = null;
            // obtem Banco com o banco
            conexao bd = new conexao();
            conn = bd.getConnection();
            cad.setnomepaciente(recebepaciente);
            cad.setmedico(recebemedico);
            cad.sethorario(recebehorario);
            cad.setsintomas(recebesintomas);
            cad.incluirconsulta(conn);
            consultapaciente();

         

        } catch (SQLException ex) {
            Logger.getLogger(PACIENTE.class.getName()).log(Level.SEVERE, null, ex);
        }
