    public void consultapaciente() {
        try {
            //CONSULTAR PACIENTES CADASTRADOS
            DefaultTableModel grid0 = (DefaultTableModel) tabelaconsultapaciente.getModel();
            grid0.setNumRows(0);
            Connection conn = null;
            conexao bd = new conexao();
            conn = bd.getConnection();
            Statement stmt = null;
            ResultSet rs = null;

            String sqlConsulta = "Select * from clientes order by idclientes";
            stmt = conn.createStatement();
            rs = stmt.executeQuery(sqlConsulta);
            while (rs.next()) {
                String recebe_cod = Integer.toString(rs.getInt("idclientes"));
                String recebe_nome = rs.getString("nome");
                String recebe_idade = Integer.toString(rs.getInt("idade"));
                String recebe_ps = rs.getString("plano_saude");
                // int recebe_rg = rs.getInt("rg");
                // Mostra os valores na JTable
                DefaultTableModel grid = (DefaultTableModel) tabelaconsultapaciente.getModel();
                grid.addRow(new String[]{recebe_cod, recebe_nome, recebe_idade, recebe_ps});

            }
            rs.close();
            stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(PACIENTE.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
